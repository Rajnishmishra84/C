#include <stdio.h>

int main() {
     int x = 8; 
     printf(" %d", x);

     // update
     x = 10;
     printf("\n %d", x);

     x = x + 7;
     printf("\n %d", x);
     printf("\n");

     //* Arithmetic operations on int Data Type

     int a = 10;
     int b = 2;
     printf("\n %d", a+b);
     printf("\n %d", a-b);
     printf("\n %d", a*b);
     printf("\n %d", a/b);

     return 0; 
} 
